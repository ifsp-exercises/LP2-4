public class Student {
  public String name;
  public int age;
  public float weight;
  public float height;
  public String objective;

  public Student() {
    super();
  }

  public Student(String name, int age, float weight, float height, String objective) {
    this.name = name;
    this.age = age;
    this.weight = weight;
    this.height = height;
    this.objective = objective;
  }
}
